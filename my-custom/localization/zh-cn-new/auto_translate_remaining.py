def translate_content(content):
    """
    简单的内容翻译函数，基于关键词映射
    """
    # 保持代码块不变
    code_blocks = re.findall(r'```[\s\S]*?```', content)
    for i, block in enumerate(code_blocks):
        content = content.replace(block, f'__CODE_BLOCK_{i}__', 1)

    # 保持YAML前置内容不变
    yaml_match = re.match(r'^---[\s\S]*?---\n', content)
    if yaml_match:
        yaml_content = yaml_match.group(0)
        body_content = content[len(yaml_content):]
    else:
        yaml_content = ''
        body_content = content

    # 简单的单词替换翻译
    words = re.findall(r'\b[a-zA-Z]+\b', body_content)
    for word in set(words):
        if word.lower() in TRANSLATION_MAP:
            # 保持大小写
            if word.isupper():
                replacement = TRANSLATION_MAP[word.lower()].upper()
            elif word[0].isupper():
                replacement = TRANSLATION_MAP[word.lower()].capitalize()
            else:
                replacement = TRANSLATION_MAP[word.lower()]

            # 只替换完整单词
            pattern = r'\b' + re.escape(word) + r'\b'
            body_content = re.sub(pattern, replacement, body_content)

    # 恢复代码块
    for i, block in enumerate(code_blocks):
        body_content = body_content.replace(f'__CODE_BLOCK_{i}__', block, 1)

    return yaml_content + body_content

